public class MarketSupplying {
    public Map<String, Integer> supplyFruits(Map<String, Integer> storage, int supply) {
        //–Ω–∞–ø–∏—à–∏—Ç–µ —Ç—É—Ç –≤–∞—à –∫–æ–¥
        for (Map.Entry<String, Integer> entry : storage.entrySet()) {
            entry.setValue(entry.getValue() + supply);
        }
        return storage;
    }
}
